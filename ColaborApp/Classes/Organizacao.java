class Organizacao{
  private String nome, cnpj;

  public Organizacao(){}

  public void setNome(String nome){
    this.nome = nome;
  }
  public String getNome(){
    return this.nome;
  }
  public void setCNPJ(String cnpj){
    this.cnpj = cnpj;
  }
  public String getCNPJ(){
    return this.cnpj;
  }
}